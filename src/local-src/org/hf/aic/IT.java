package org.hf.aic;

public final class IT {
	
	private String name;
	private double discount;
    private double yield;
    private double ter;
    private double perfY1;
    private double perfY3;
    private double perfY5;
    
    
	public final String getName() {
		return name;
	}
	
	public final void setName(String name) {
		this.name = name;
	}
	
	public final double getDiscount() {
		return discount;
	}
	
	public final void setDiscount(double discount) {
		this.discount = discount;
	}
	
	public final double getYield() {
		return yield;
	}
	
	public final void setYield(double yield) {
		this.yield = yield;
	}
	public final double getTer() {
		return ter;
	}
	public final void setTer(double ter) {
		this.ter = ter;
	}
	public final double getPerfY1() {
		return perfY1;
	}
	public final void setPerfY1(double perfY1) {
		this.perfY1 = perfY1;
	}
	public final double getPerfY3() {
		return perfY3;
	}
	public final void setPerfY3(double perfY3) {
		this.perfY3 = perfY3;
	}
	public final double getPerfY5() {
		return perfY5;
	}
	public final void setPerfY5(double perfY5) {
		this.perfY5 = perfY5;
	}
    
    
}
